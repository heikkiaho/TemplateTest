#include "PropertyHolder.h"
#include <stdexcept>
using namespace std;
////////////////////////////////////////////////////////////////////////////////
Property &
PropertyHolder::GetProperty(const std::string & id ) throw (std::runtime_error) {
  Properties::iterator it = m_Properties.find(id);

  if ( it == m_Properties.end()) throw runtime_error(std::string("no such property ") + std::string(id) );
  return *(*it).second;
}
////////////////////////////////////////////////////////////////////////////////
Properties &
PropertyHolder::GetProperties()
{
  return m_Properties;
}
